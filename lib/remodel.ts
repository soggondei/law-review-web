export interface RemodelParams {
  연면적: number;
  층수: number;
  용도: string;
  준공연도?: string;
  해체: boolean;
  전체해체: boolean;
  리모델링활성화: boolean;
  체크항목: number[];
}

export interface RemodelItem {
  항목: string;
  법령: string;
  결과: string;
  상태: "danger" | "warning" | "info" | "ok" | "check";
  내용: any[];
  비고?: string;
}

export interface RemodelResult {
  분석결과: RemodelItem[];
  요약: { 대수선해당: boolean; 허가신고: string; 해당항목수: number; 주의사항수: number };
}

const 대수선범위 = [
  { no:1, 항목:"내력벽 수선",          기준:"내력벽을 증설·해체하거나 벽면적 30㎡ 이상 수선·변경",           법령:"시행령 제3조의2 제1호" },
  { no:2, 항목:"기둥 수선",            기준:"기둥을 증설·해체하거나 3개 이상 수선·변경",                    법령:"시행령 제3조의2 제2호" },
  { no:3, 항목:"보 수선",              기준:"보를 증설·해체하거나 3개 이상 수선·변경",                      법령:"시행령 제3조의2 제3호" },
  { no:4, 항목:"지붕틀 수선",          기준:"지붕틀을 증설·해체하거나 3개 이상 수선·변경 (한옥 서까래 제외)", 법령:"시행령 제3조의2 제4호" },
  { no:5, 항목:"방화벽·방화구획 수선", 기준:"방화벽 또는 방화구획 바닥·벽을 증설·해체·수선·변경",           법령:"시행령 제3조의2 제5호" },
  { no:6, 항목:"계단 수선",            기준:"주계단·피난계단·특별피난계단을 증설·해체·수선·변경",            법령:"시행령 제3조의2 제6호" },
  { no:7, 항목:"경계벽 수선(공동주택)", 기준:"다가구·다세대주택 세대간 경계벽을 증설·해체·수선·변경",         법령:"시행령 제3조의2 제8호" },
  { no:8, 항목:"건축물 높이 변경",     기준:"건축물 높이를 3m 이상 증가",                                  법령:"시행령 제3조의2 제9호" },
  { no:9, 항목:"외벽 마감재료 수선",   기준:"외벽 마감재료를 벽면적 30㎡ 이상 증설·해체·수선·변경",         법령:"시행령 제3조의2 제9호" },
];

export function analyzeRemodel(params: RemodelParams): RemodelResult {
  const { 연면적, 층수, 용도, 준공연도 = "", 해체, 전체해체, 리모델링활성화, 체크항목 } = params;
  const 공동주택 = ["공동주택","아파트","연립주택","다세대주택"].some(u => 용도.includes(u));
  const results: RemodelItem[] = [];

  // ── 1. 대수선 해당 여부 ────────────────────────────────────────────────────
  const 해당항목목록 = 대수선범위.filter(i => 체크항목.includes(i.no));
  const 대수선해당 = 해당항목목록.length > 0;

  results.push({
    항목: "대수선 해당 여부",
    법령: "건축법 제2조 제1항 제9호, 시행령 제3조의2",
    결과: 대수선해당 ? `해당 (${해당항목목록.length}개 항목)` : "미해당 — 체크항목 없음",
    상태: 대수선해당 ? "warning" : "check",
    내용: 대수선범위.map(i => ({ no: i.no, 항목: i.항목, 기준: i.기준, 법령: i.법령, 해당: 체크항목.includes(i.no) })),
    비고: "9개 항목 중 1개 이상 해당 시 대수선. 증축·개축·재축에 해당하는 것은 제외",
  });

  // ── 2. 허가 / 신고 ─────────────────────────────────────────────────────────
  const 신고소규모 = 연면적 > 0 && 연면적 < 200 && 층수 > 0 && 층수 < 3;
  const 허가신고 = 신고소규모 ? "신고" : 대수선해당 ? "허가" : "확인 필요";

  results.push({
    항목: "허가 / 신고 구분",
    법령: "건축법 제11조(허가), 제14조(신고)",
    결과: 허가신고,
    상태: 허가신고 === "허가" ? "danger" : "info",
    내용: [
      { 구분: "신고 대상", 조건: "연면적 200㎡ 미만 + 3층 미만 건축물의 대수선",         해당: 신고소규모 },
      { 구분: "허가 대상", 조건: "위 신고 대상 외 모든 대수선",                          해당: !신고소규모 && 대수선해당 },
    ],
    비고: "허가 대상은 관할 구청 건축과에 건축허가 신청 필요",
  });

  // ── 3. 해체신고 / 해체허가 ────────────────────────────────────────────────
  if (해체 || 전체해체) {
    const 신고_부분해체 = 해체 && !전체해체;
    const 신고_소규모전체 = 전체해체 && 연면적 < 500 && 층수 <= 3;
    const 해체결과 = (신고_부분해체 || 신고_소규모전체) ? "해체신고" : "해체허가";

    results.push({
      항목: "해체신고 / 해체허가",
      법령: "건축물관리법 제30조",
      결과: 해체결과,
      상태: 해체결과 === "해체허가" ? "danger" : "info",
      내용: [
        { 구분: "해체신고", 조건: "주요구조부 해체 없이 건축물 일부 해체",                  해당: 신고_부분해체 },
        { 구분: "해체신고", 조건: "연면적 500㎡ 미만 + 3층 이하 전체 해체",                해당: 신고_소규모전체 },
        { 구분: "해체허가", 조건: "위 신고 대상 외 건축물 해체",                           해당: !신고_부분해체 && !신고_소규모전체 },
      ],
      비고: "해체감리자 지정 및 착공 전 신고·허가 필수",
    });

    // 해체감리
    const 감리필요 = !신고_부분해체;
    results.push({
      항목: "해체감리 지정",
      법령: "건축물관리법 제31조",
      결과: 감리필요 ? "지정 필요" : "확인 필요",
      상태: 감리필요 ? "warning" : "info",
      내용: [
        { 구분: "해체감리 대상", 조건: "해체허가 대상 건축물",   해당: !신고_부분해체 && !신고_소규모전체 },
        { 구분: "해체감리 대상", 조건: "해체신고 중 밀집지역 등 허가권자 판단", 해당: 신고_부분해체 },
      ],
      비고: "허가권자가 안전한 해체를 위해 필요하다고 판단하는 경우 지정",
    });
  }

  // ── 4. 공사감리 ───────────────────────────────────────────────────────────
  const 공사감리 = 허가신고 === "허가";
  const 지정감리 = !공사감리 && 연면적 >= 200;
  results.push({
    항목: "공사감리 / 지정감리",
    법령: "건축법 제25조, 시행령 제19조",
    결과: 공사감리 ? "공사감리 (건축사)" : 지정감리 ? "지정감리 검토 필요" : "해당 없음",
    상태: 공사감리 ? "danger" : 지정감리 ? "warning" : "ok",
    내용: [
      { 구분: "공사감리", 조건: "건축허가 대상 건축물",                    해당: 공사감리 },
      { 구분: "지정감리", 조건: "소규모 건축물 중 연면적 200㎡ 이상",       해당: 지정감리 },
    ],
    비고: "공사감리자는 건축사, 지정감리는 허가권자가 건축사를 지정",
  });

  // ── 5. 건축물관리계획서 ───────────────────────────────────────────────────
  const 면제 = ["공장","지식산업센터","학교","단독주택"].some(u => 용도.includes(u)) || 공동주택;
  results.push({
    항목: "건축물관리계획서 제출",
    법령: "건축물관리법 제11조",
    결과: 면제 ? "제출 면제" : "사용승인 시 제출",
    상태: 면제 ? "ok" : "info",
    내용: [
      { 구분: "면제", 조건: "공장·지식산업센터·학교·단독주택·의무관리 공동주택", 해당: 면제 },
    ],
    비고: "건축물관리계획서 미제출 시 사용승인 지연 가능",
  });

  // ── 6. 건설사업자 시공 ────────────────────────────────────────────────────
  const 시공의무 = 연면적 > 200 || 공동주택;
  results.push({
    항목: "건설사업자 시공 의무",
    법령: "건설산업기본법 제41조",
    결과: 시공의무 ? "종합건설업체 시공 필수" : "확인 필요",
    상태: 시공의무 ? "warning" : "info",
    내용: [
      { 구분: "의무", 조건: "연면적 200㎡ 초과 건축물", 해당: 연면적 > 200 },
      { 구분: "의무", 조건: "공동주택·다중주택·다가구주택", 해당: 공동주택 },
    ],
    비고: "건설업 등록 업체에 의한 시공 필수. 위반 시 사용승인 불가",
  });

  // ── 7. 구조안전확인서 ─────────────────────────────────────────────────────
  const 구조안전 = 층수 >= 2 || 연면적 >= 200;
  results.push({
    항목: "구조안전확인서",
    법령: "건축법 시행령 제32조",
    결과: 구조안전 ? "제출 필요" : "해당 없음",
    상태: 구조안전 ? "warning" : "ok",
    내용: [
      { 구분: "대상", 조건: "2층 이상 건축물",        해당: 층수 >= 2 },
      { 구분: "대상", 조건: "연면적 200㎡ 이상 건축물", 해당: 연면적 >= 200 },
    ],
    비고: "건축구조기술사 서명 구조계산서 첨부 필요",
  });

  // ── 8. 면적 기반 필요 서류 ────────────────────────────────────────────────
  const 서류목록: { 서류: string; 기준: string; 법령: string }[] = [];
  if (연면적 >= 500)  서류목록.push({ 서류: "에너지절약계획서",      기준: "연면적 500㎡↑",        법령: "녹색건축물법 제14조" });
  if (연면적 >= 3000) 서류목록.push({ 서류: "녹색건축인증 검토",     기준: "연면적 3,000㎡↑ 공공",  법령: "녹색건축물법 제16조" });
  if (연면적 >= 500)  서류목록.push({ 서류: "에너지효율등급 인증",   기준: "연면적 500㎡↑",        법령: "녹색건축물법 제17조" });
  if (층수 >= 5 || 연면적 >= 500) 서류목록.push({ 서류: "장애인 편의시설 검토", 기준: "용도·면적별 기준", 법령: "장애인편의법 시행령 별표2" });
  results.push({
    항목: "면적 기반 필요 서류",
    법령: "각 개별법",
    결과: 서류목록.length > 0 ? `${서류목록.length}개 항목 검토 필요` : "해당 없음",
    상태: 서류목록.length > 0 ? "warning" : "ok",
    내용: 서류목록,
    비고: "대수선 범위에 따라 적용 여부 달라질 수 있음",
  });

  // ── 9. 기존 건축물 특례 ───────────────────────────────────────────────────
  results.push({
    항목: "기존 건축물 특례",
    법령: "건축법 제6조, 시행령 제6조의2",
    결과: "현황 건폐율·용적률·건축선·대지안의 공지 유지",
    상태: "info",
    내용: [
      { 구분: "유지", 조건: "건폐율 — 현황 유지 (증축 시 적합 부분만 법령 적용)" },
      { 구분: "유지", 조건: "용적률 — 현황 유지 (증축 시 적합 부분만 법령 적용)" },
      { 구분: "유지", 조건: "건축선·대지안의 공지 — 기존 상태 유지" },
    ],
    비고: "건축물대장과 현황 건축물 일치 여부 사전 확인 필수",
  });

  // ── 10. 리모델링 특례 ─────────────────────────────────────────────────────
  const 경과년 = 준공연도 ? new Date().getFullYear() - parseInt(준공연도.slice(0, 4)) : 0;
  const 완화가능 = 경과년 >= 15 || 리모델링활성화;
  results.push({
    항목: "적용 완화 (리모델링 특례)",
    법령: "건축법 시행령 제6조 제1항 제6호",
    결과: 완화가능 ? "완화 신청 가능 (건축심의)" : "해당 없음",
    상태: 완화가능 ? "info" : "ok",
    내용: [
      { 구분: "완화 대상", 조건: "사용승인 후 15년 이상 경과",         해당: 경과년 >= 15, 값: 경과년 > 0 ? `경과 ${경과년}년` : "준공연도 미입력" },
      { 구분: "완화 대상", 조건: "리모델링 활성화 구역 내 건축물",      해당: 리모델링활성화 },
    ],
    비고: "건축심의를 통해 건폐율·용적률·높이 완화 신청 가능",
  });

  const 주의사항수 = results.filter(r => r.상태 === "danger" || r.상태 === "warning").length;

  return {
    분석결과: results,
    요약: { 대수선해당, 허가신고, 해당항목수: 해당항목목록.length, 주의사항수 },
  };
}
