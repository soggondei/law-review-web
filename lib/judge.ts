export function calcParking(용도: string, 연면적: number, 세대수 = 0) {
  // 공동주택: 세대수 입력 시 세대수 기반, 미입력 시 면적 기반 병기
  for (const [key, std] of Object.entries(PARKING_세대수)) {
    if (용도.includes(key)) {
      const 면적기준대수 = Math.ceil(연면적 / 75);
      if (세대수 > 0) {
        const 세대기준대수 = Math.ceil(세대수 * std.perUnit);
        const 적용대수 = Math.max(세대기준대수, 면적기준대수);
        return {
          대수: 적용대수,
          기준: `세대당 ${std.perUnit}대 × ${세대수}세대 = ${세대기준대수}대 (면적기준 ${면적기준대수}대, 큰 값 적용)`,
          근거: std.근거,
        };
      }
      return { 대수: 면적기준대수, 기준: `연면적 75㎡당 1대 (세대수 입력 시 세대당 기준 적용)`, 근거: std.근거 };
    }
  }
  for (const [key, std] of Object.entries(PARKING)) {
    if (용도.includes(key.replace("시설",""))) {
      const 대수 = Math.ceil(연면적 / std.per);
      return { 대수, 기준: `연면적 ${std.per}㎡당 1대`, 근거: std.근거 };
    }
  }
  return null;
}

